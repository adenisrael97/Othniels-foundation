export async function generateMetadata(props) {
  const params = await props.params;
  const { id } = params;
  const post = blogPosts[id];

  if (!post) {
    return {
      title: "Blog Post Not Found | Othniel's Foundation",
      description: "The blog post you are looking for does not exist on Othniel's Foundation.",
      alternates: {
        canonical: "/blog",
      },
    };
  }

  const siteTitle = "Othniel's Foundation";
  const url = `/blog/${id}`;
  const description = post.description || "Read the latest story from Othniel's Foundation about how we are transforming lives and communities.";

  return {
    title: `${post.title} | ${siteTitle}`,
    description,
    alternates: {
      canonical: url,
    },
    openGraph: {
      title: `${post.title} | ${siteTitle}`,
      description,
      type: "article",
      url,
      images: [
        {
          url: post.image,
          alt: post.title,
        },
      ],
    },
    twitter: {
      card: "summary_large_image",
      title: `${post.title} | ${siteTitle}`,
      description,
      images: [post.image],
    },
  };
}

